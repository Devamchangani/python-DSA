l1 = [1, 57, 55, 95, 8]

# Sorted  list 
l1.sort()
print(l1)

# Reverse list
l1.reverse()
print(l1)

#adds 65 at the end of the list  - Append list
l1.append(65)
print(l1)

# insert 6853  at index 0
l1.insert(0, 6853)
print(l1)

#remove element at index 2
l1.pop(2)
print(l1)

# remove 95 from the list
l1.remove(95)
print(l1)